<?php

class LoginClienteVO {
	
	public $retorno;
	public $mensagem;
	public $objeto;
	
	public function __construct($retorno, $mensagem, $objeto){
		$this->retorno  	= isset($retorno) 	? $retorno 	: false;
		$this->mensagem 	= isset($mensagem) 	? $mensagem : array();
		$this->objeto 		= isset($objeto) 	? $objeto 	: new stdClass();
	}
	
}